    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblAirline.getSelectedRow();
        if(selectedRow>=0){
            int selectionButton = JOptionPane.YES_NO_OPTION;
            int selectionResult = JOptionPane.showConfirmDialog(null, "Are you sure to delete??","Warning",selectionButton);
            if(selectionResult == JOptionPane.YES_OPTION){
             Airline airline = (Airline)tblAirline.getValueAt(selectedRow, 1);
             Flight flight=(Flight)tblAirline.getValueAt(selectedRow, 4);
             airlineList.getAirlineList().remove(airline);
             FlightDirectory flightList=airline.getFlightDirectory();
             flightList.getFlightList().remove(flight);
             
             List<Ticket> list;
                list = Collections.synchronizedList(new ArrayList<Ticket>(ticketList.getTicketList()));
                synchronized(list) {
                    Iterator<Ticket> iter = list.iterator();
                    while (iter.hasNext()) { 
                        Ticket t = iter.next();
                        if(t.getFlight().equals(flight)) {
                            ticketList.getTicketList().remove(t);
                        }
                    } 
                }
             populateTable();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


