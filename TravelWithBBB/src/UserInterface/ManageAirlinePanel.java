     public void populateTable(){
        DefaultTableModel dtm=(DefaultTableModel) tblAirline.getModel();
        dtm.setRowCount(0);
        for(Airline airline:airlineList.getAirlineList()){
            Object[] row=new Object[6];
            row[0]=airline.getId();
            row[1]=airline;
            for(Flight flight:airline.getFlightDirectory().getFlightList()){
                row[2]=flight.getFrom();
                row[3]=flight.getTo();
                row[4]=flight;
                row[5]=flight.getArrival();
                dtm.addRow(row);
            }
        }
    }
    
    
